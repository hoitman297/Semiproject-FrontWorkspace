CREATE TABLE "CopyOfUntitled" (
	"Field"	VARCHAR2(255)		NULL
);

CREATE TABLE "Untitled" (
	"NOTICE_NUM"	VARCHAR2(10)		NOT NULL,
	"TITLE"	VARCHAR2(50)		NOT NULL,
	"WRITE_DATE"	DATE	DEFAULT SYSDATE	NOT NULL,
	"Field"	VARCHAR2(255)		NULL,
	"USER_ID"	VARCHAR2(20)		NOT NULL,
    CONSTRAINT "PK_UNTITLED" PRIMARY KEY ("NOTICE_NUM")
);

COMMENT ON COLUMN "Untitled"."NOTICE_NUM" IS '공지사항번호';
COMMENT ON COLUMN "Untitled"."TITLE" IS '글 제목';
COMMENT ON COLUMN "Untitled"."WRITE_DATE" IS '디폴트 : SYSDATE';
COMMENT ON COLUMN "Untitled"."USER_ID" IS '아이디(PK)';

CREATE TABLE "USER" (
	"USER_ID"	VARCHAR2(20)		NOT NULL,
	"USER_PW"	NVARCHAR2(50)		NOT NULL,
	"USER_NAME"	VARCHAR2(50)		NOT NULL, -- 크기 지정 필요
	"STATUS"	CHAR(1)	DEFAULT 'Y'	NOT NULL, -- 문자열 리터럴 'Y'로 수정
	"WITHDRAW"	NUMBER(1)		NOT NULL, -- bit(1)은 오라클에 없음, NUMBER(1)로 대체
	"ENROLL_DATE"	DATE	DEFAULT SYSDATE	NOT NULL,
    CONSTRAINT "PK_USER" PRIMARY KEY ("USER_ID")
);

COMMENT ON COLUMN "USER"."USER_ID" IS '아이디(PK)';
COMMENT ON COLUMN "USER"."USER_PW" IS '비밀번호';
COMMENT ON COLUMN "USER"."USER_NAME" IS '닉네임';
COMMENT ON COLUMN "USER"."STATUS" IS '로그인 Y , N';
COMMENT ON COLUMN "USER"."WITHDRAW" IS '탈퇴여부';
COMMENT ON COLUMN "USER"."ENROLL_DATE" IS '디폴트 : SYSDATE';

CREATE TABLE "REPORT" (
	"Field"	VARCHAR2(255)		NULL,
	"Field2"	VARCHAR2(255)		NULL,
	"Field3"	VARCHAR2(255)		NULL
);

CREATE TABLE "LOL_BOARD" (
