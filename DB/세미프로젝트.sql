	"USER_NAME"	VARCHAR2(50)		NOT NULL,
	"STATUS"	CHAR(1)		NOT NULL,
	"ENROLL_DATE"	DATE	DEFAULT SYSDATE	NOT NULL,
	"Field"	VARCHAR2(255)		NULL,
    CONSTRAINT "PK_LOL_BOARD" PRIMARY KEY ("BOARD_NO")
);

COMMENT ON COLUMN "LOL_BOARD"."BOARD_NO" IS '시퀀스 자동생성';
COMMENT ON COLUMN "LOL_BOARD"."USER_PW" IS '비밀번호';
COMMENT ON COLUMN "LOL_BOARD"."USER_NAME" IS '닉네임';
COMMENT ON COLUMN "LOL_BOARD"."STATUS" IS '로그인 유무, 탈퇴';
COMMENT ON COLUMN "LOL_BOARD"."ENROLL_DATE" IS '디폴트 : SYSDATE';

